

#ifndef PLATFORMTYPES_H_
#define PLATFORMTYPES_H_
#include "stdio.h"

typedef char int8_t;
typedef unsigned char uint8_t;

typedef short int16_t;
typedef unsigned short uint16_t;

typedef long int int32_t;
typedef unsigned long int uint32_t;

typedef long long int int64_t;
typedef unsigned long long int uint64_t;

typedef float float32_t;

typedef double float64_t;

typedef long double float96_t;


typedef volatile char vint8_t;
typedef volatile unsigned char vuint8_t;

typedef volatile short vint16_t;
typedef volatile unsigned short vuint16_t;

typedef volatile long int vint32_t;
typedef volatile unsigned long int vuint32_t;

typedef volatile long long int vint64_t;
typedef volatile unsigned long long int vuint64_t;

typedef volatile float vfloat32_t;

typedef volatile double vfloat64_t;

typedef volatile long double vfloat96_t;


#endif /* PLATFORMTYPES_H_ */
